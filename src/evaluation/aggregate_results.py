#!/usr/bin/env python3
"""
AMBC Result Aggregation & Pareto Frontier Visualization
============================================================
Reads all experiments/*/outputs*/results.json files and generates:
1. A Markdown summary table (Standard Track)
2. A Pareto frontier plot (F1 vs Latency, log-scale X-axis)

Usage:
    python src/evaluation/aggregate_results.py

Output:
    - src/evaluation/AMBC_summary_table.md
    - src/evaluation/AMBC_pareto_frontier.png
"""

import json
import os
from pathlib import Path

import numpy as np
import matplotlib.pyplot as plt


def load_all_results(base_dir: str = "experiments"):
    """Walk experiments/*/outputs*/results.json and collect summaries."""
    results = []
    base = Path(base_dir)
    for json_path in sorted(base.glob("*/outputs*/results.json")):
        with open(json_path, "r", encoding="utf-8") as f:
            data = json.load(f)

        model_name = data.get("model", json_path.parent.parent.name)

        results.append({
            "model": model_name,
            "f1": data["summary"].get("mean_f1", 0.0),
            "f1_std": data["summary"].get("std_f1", 0.0),
            "acc": data["summary"].get("mean_acc", 0.0),
            "acc_std": data["summary"].get("std_acc", 0.0),
            "latency_ms": data["summary"].get("mean_latency_ms", 0.0),
            "latency_std": data["summary"].get("std_latency_ms", 0.0),
            "peak_mem_mb": data["summary"].get("mean_peak_memory_mb", 0.0),
            "n_params": data["summary"].get("n_params", 0),
            "hardware_note": data.get("hardware_note", ""),
            "protocol": data.get("protocol", "AMBC-Standard-v1.0"),
            "source_path": str(json_path),
        })
    return results


def compute_pareto_frontier(points):
    """
    Dominance: higher F1 AND lower latency is better.
    Returns non-dominated points sorted by latency ascending.
    """
    sorted_pts = sorted(points, key=lambda p: (p["latency_ms"], -p["f1"]))
    frontier = []
    max_f1_so_far = -1.0
    for p in sorted_pts:
        if p["f1"] > max_f1_so_far:
            frontier.append(p)
            max_f1_so_far = p["f1"]
    return frontier


def generate_markdown_table(results, output_path: str):
    """Write Standard Track table to Markdown."""
    lines = []
    lines.append("# AMBC Phase 1 Results Summary\n")
    lines.append("## Standard Track (Directly Comparable)\n")
    lines.append("| # | Model | F1 | Acc. | Latency (ms) | GPU Mem (MB) | Notes |")
    lines.append("|---|-------|------|------|--------------|--------------|-------|")

    std_results = sorted(results, key=lambda r: r["f1"], reverse=True)
    for i, r in enumerate(std_results, 1):
        f1_str = f"{r['f1']:.4f} ± {r['f1_std']:.4f}" if r["f1_std"] else f"{r['f1']:.4f}"
        acc_str = f"{r['acc']:.4f} ± {r['acc_std']:.4f}" if r["acc_std"] else f"{r['acc']:.4f}"
        lat_str = f"{r['latency_ms']:.3f} ± {r['latency_std']:.3f}" if r["latency_std"] else f"{r['latency_ms']:.3f}"
        mem_str = f"{r['peak_mem_mb']:.1f}" if r["peak_mem_mb"] else "—"
        note = r["hardware_note"][:55] + "..." if len(r["hardware_note"]) > 55 else r["hardware_note"]
        if not note:
            note = "—"
        lines.append(
            f"| {i} | {r['model']} | {f1_str} | {acc_str} | {lat_str} | {mem_str} | {note} |"
        )

    lines.append("\n")
    lines.append("*Generated by `src/evaluation/aggregate_results.py`*\n")

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    print(f"Markdown table saved -> {output_path}")


def plot_pareto_frontier(results, output_path: str):
    """Draw F1 vs Latency scatter + Pareto frontier line."""
    fig, ax = plt.subplots(figsize=(10, 6))

    latencies = [r["latency_ms"] for r in results]
    f1s = [r["f1"] for r in results]
    models = [r["model"] for r in results]

    colors = plt.cm.tab10(np.linspace(0, 1, len(results)))

    for i, (lat, f1, model, color) in enumerate(zip(latencies, f1s, models, colors)):
        ax.scatter(lat, f1, s=25, c=[color], alpha=0.9,
                   edgecolors="black", linewidth=0.6, zorder=3)
        offset = (8, 4) if i % 2 == 0 else (8, -12)
        ax.annotate(
            model,
            (lat, f1),
            textcoords="offset points",
            xytext=offset,
            fontsize=8,
            fontweight="normal",
            zorder=4,
        )

    frontier = compute_pareto_frontier(results)
    if len(frontier) >= 2:
        frontier_lat = [p["latency_ms"] for p in frontier]
        frontier_f1 = [p["f1"] for p in frontier]
        ax.plot(frontier_lat, frontier_f1, "k--",
                linewidth=1.8, label="Pareto Frontier", zorder=2)

    ax.set_xscale("log")
    ax.set_xlabel("Inference Latency (ms, log scale)", fontsize=12)
    ax.set_ylabel("F1-Score", fontsize=12)
    ax.set_title("AMBC Phase 1: Pareto Frontier (F1 vs Latency)",
                 fontsize=14, fontweight="bold")
    ax.grid(True, which="both", linestyle=":", alpha=0.5)
    ax.legend(loc="lower right")
    ax.set_ylim(0.5, 0.85)

    plt.tight_layout()
    plt.savefig(output_path, dpi=300, bbox_inches="tight")
    plt.close()
    print(f"Pareto plot saved -> {output_path}")


def main():
    script_dir = Path(__file__).resolve().parent
    project_root = script_dir.parents[1] if script_dir.name == "evaluation" else Path(".")
    os.chdir(project_root)

    results = load_all_results("experiments")
    if not results:
        print("No results.json found under experiments/*/outputs*/")
        return

    print(f"Loaded {len(results)} result files.")
    for r in results:
        print(f"  - {r['model']}: F1={r['f1']:.4f}, Latency={r['latency_ms']:.3f}ms")

    out_dir = script_dir
    out_dir.mkdir(parents=True, exist_ok=True)

    md_path = out_dir / "AMBC_summary_table.md"
    png_path = out_dir / "AMBC_pareto_frontier.png"

    generate_markdown_table(results, str(md_path))
    plot_pareto_frontier(results, str(png_path))

    print("\nDone.")


if __name__ == "__main__":
    main()